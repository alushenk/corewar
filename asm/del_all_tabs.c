#include "lkorvar.h"

void	del_tab(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '\t')
			s[i] = ' ';
		i++;
	}
	printf("s = %s\n", s);
}
