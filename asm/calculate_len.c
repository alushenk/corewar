#include "lkorvar.h"

void	calc(char **data)
{
	int		i;
	int		j;
	char	*f;

	i = 0;
	j = 4384;
	while ((*data)[i] != '*')
		i++;
	while ((*data)[j] != '\0')
		j++;
	f = pres_4((j - 4384) / 2);
	while (*f != '\0')
	{
		(*data)[i] = *f;
		f++;
		i++;
	}
}
