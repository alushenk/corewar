#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*mem;
	size_t	i;

	mem = (char *)s;
	i = 0;
	while (i < n)
	{
		mem[i] = (char)c;
		i++;
	}
	return (s);
}
